package bird;
public class Bird {

        int weight;
        String name;
        String color;

        String getColor() {
            return this.color;
        }
        int getWeight() {
            return this.weight;
        }
        String getName() {
            return this.name;
        }

        void setName(String name) {
            this.name = name;
        }

        void setWeight(int weight) {
            this.weight = weight;
        }

        void setColor(String color) {
            this.color = color;
        }
    }


